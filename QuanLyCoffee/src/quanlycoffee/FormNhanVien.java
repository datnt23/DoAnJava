    private void btnThemNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNVActionPerformed
        StringBuffer sb = new StringBuffer();
        if (txtMaNV.getText().equals("") || txtTenNV.getText().equals("") || txtSDTNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            //Bẫy lỗi nhập thiếu dữ liệu.
        } else {
            if(txtMaNV.getText().trim().length() > 6){
                JOptionPane.showMessageDialog(this, "Mã nhân viên không vượt quá 6 ký tự! ");
                txtMaNV.grabFocus();
            }
            if (txtSDTNV.getText().trim().length() > 11 || txtSDTNV.getText().trim().length() < 10) {
                JOptionPane.showMessageDialog(this, "Số điện thoại gồm 10-11 số! ");
                txtSDTNV.grabFocus();
                return;
            } else if (!txtSDTNV.getText().trim().matches("0[1-9]{1}\\d{8,9}")) {
                JOptionPane.showMessageDialog(this, "Không đúng định dạng số điện thoại !");
                txtSDTNV.grabFocus();
                return;
            }
            //Ngược lại thêm bẫy lỗi nếu trùng khoá chính.

            if (sb.length() > 0) {    //Nếu if trên đúng thì sẽ thêm vào sb 1 đoạn string
                //Kiểm tra độ dài chuỗi này nếu lớn hơn  0 tức là có thông báo
                JOptionPane.showMessageDialog(this, sb);
                return;
            } else {
                try {
                    NhanVien nv = new NhanVien();
                    nv.setMaNV(txtMaNV.getText());
                    nv.setMaCV(cbxChucVu.getSelectedItem().toString());
                    nv.setTenNV(txtTenNV.getText());
                    nv.setSDT(txtSDTNV.getText());
                    nv.setGT((byte) (rbtNam.isSelected() ? 1 : 0));

                    NhanVienDAO nvDao = new NhanVienDAO();
                    nvDao.insert(nv);
                    JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
                    lamTrong();
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên bị trùng lặp!");
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnThemNVActionPerformed

