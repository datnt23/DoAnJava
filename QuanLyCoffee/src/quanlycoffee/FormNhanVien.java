    public boolean blockButton(boolean x){
        if(x == true){
            btnSuaNV.setEnabled(false);
            btnXoaNV.setEnabled(false);
            btnThemNV.setEnabled(true);
            btnThoatNV.setEnabled(true);
            return true;
        }else{
            btnSuaNV.setEnabled(true);
            btnXoaNV.setEnabled(true);
            btnThemNV.setEnabled(false);
            btnThoatNV.setEnabled(false);
            return false;
        }
    }
    
    public void loadComboBox() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            PreparedStatement ps = con.prepareStatement("Select MaCV from ChucVu group by MaCV");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                cbxChucVu.addItem(rs.getString("MaCV"));
            }
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }

    private void initTable() {
        tblModelNV = new DefaultTableModel();
        tblModelNV.setColumnIdentifiers(new String[]{"MaNV", "MaCV", "TenNV", "GT", "SDT"});
        tblNhanVien.setModel(tblModelNV);
    }

    public void loadData() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            String sql = "Select * from NhanVien";
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblModelNV.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaNV"), rs.getString("MaCV"), rs.getString("TenNV"), rs.getString("GT"), rs.getString("SDT")
                };
                tblModelNV.addRow(row);
            }
            tblModelNV.fireTableDataChanged();
            rs.close();
            pstmt.close();
            con.close();
            blockButton(true);
            txtMaNV.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
            ex.printStackTrace();
        }
    }

