    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int row = tblNhanVien.getSelectedRow();
        txtMaNV.setEnabled(false);
        blockButton(false);
        if (row >= 0) {
            txtMaNV.setText(tblNhanVien.getValueAt(row, 0) + "");
            cbxChucVu.setSelectedItem(tblNhanVien.getModel().getValueAt(row, 1) + "");
            txtTenNV.setText(tblNhanVien.getValueAt(row, 2) + "");
            if (tblNhanVien.getValueAt(row, 3).toString().equals("1")) {
                rbtNam.setSelected(true);
                rbtNu.setSelected(false);
            } else {
                rbtNam.setSelected(false);
                rbtNu.setSelected(true);
            }
            txtSDTNV.setText(tblNhanVien.getValueAt(row, 4) + "");
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

