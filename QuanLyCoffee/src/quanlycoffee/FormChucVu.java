    private void tblChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChucVuMouseClicked
        int row = tblChucVu.getSelectedRow();
        txtMaCV.setEnabled(false);
        blockButton(false);
        if (row >= 0) {
            txtMaCV.setText(tblChucVu.getValueAt(row, 0) + "");
            txtTenCV.setText(tblChucVu.getValueAt(row, 1) + "");
        }
        
        
        
    }//GEN-LAST:event_tblChucVuMouseClicked

    private void btnThemCVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemCVActionPerformed
        StringBuffer sb = new StringBuffer();
        if (txtMaCV.getText().equals("") || txtTenCV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            //Bẫy lỗi nhập thiếu dữ liệu.
        } else {
            if (txtMaCV.getText().trim().length() > 6) {
                JOptionPane.showMessageDialog(this, "Mã chức vụ không vượt quá 6 ký tự! ");
                txtMaCV.grabFocus();
            }
            //Ngược lại thêm bẫy lỗi nếu trùng khoá chính.

            if (sb.length() > 0) {    //Nếu if trên đúng thì sẽ thêm vào sb 1 đoạn string
                //Kiểm tra độ dài chuỗi này nếu lớn hơn  0 tức là có thông báo
                JOptionPane.showMessageDialog(this, sb);
                return;
            } else {
                try {
                    ChucVu cv = new ChucVu();
                    cv.setMaCV(txtMaCV.getText());
                    cv.setTenCV(txtTenCV.getText());

                    ChucVuDAO cvDao = new ChucVuDAO();
                    cvDao.insert(cv);
                    JOptionPane.showMessageDialog(this, "Thêm chức vụ thành công!");
                    lamTrong();
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Mã chức vụ bị trùng lặp!");
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnThemCVActionPerformed

