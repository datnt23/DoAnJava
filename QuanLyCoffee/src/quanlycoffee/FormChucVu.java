    public boolean blockButton(boolean x) {
        if (x == true) {
            btnSuaCV.setEnabled(false);
            btnXoaCV.setEnabled(false);
            btnThemCV.setEnabled(true);
            btnThoatCV.setEnabled(true);
            return true;
        } else {
            btnSuaCV.setEnabled(true);
            btnXoaCV.setEnabled(true);
            btnThemCV.setEnabled(false);
            btnThoatCV.setEnabled(false);
            return false;
        }
    }

    private void initTable() {
        tblModelCV = new DefaultTableModel();
        tblModelCV.setColumnIdentifiers(new String[]{"MaCV", "TenCV"});
        tblChucVu.setModel(tblModelCV);
    }

    public void loadData() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            String sql = "Select * from ChucVu";
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblModelCV.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaCV"), rs.getString("TenCV")
                };
                tblModelCV.addRow(row);
            }
            tblModelCV.fireTableDataChanged();
            rs.close();
            pstmt.close();
            con.close();
            blockButton(true);
            txtMaCV.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
            ex.printStackTrace();
        }
    }

