    private void tblLoaiDoUongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaiDoUongMouseClicked
        int row = tblLoaiDoUong.getSelectedRow();
        txtMaLoai.setEnabled(false);
        blockButton(false);
        if (row >= 0) {
            txtMaLoai.setText(tblLoaiDoUong.getValueAt(row, 0) + "");
            txtTenLoai.setText(tblLoaiDoUong.getValueAt(row, 1) + "");
        }
    }//GEN-LAST:event_tblLoaiDoUongMouseClicked

    private void btnThemLDUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLDUActionPerformed
        StringBuffer sb = new StringBuffer();
        if (txtMaLoai.getText().equals("") || txtTenLoai.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            //Bẫy lỗi nhập thiếu dữ liệu.
        } else {
            if (txtMaLoai.getText().trim().length() > 6) {
                JOptionPane.showMessageDialog(this, "Mã loại đồ uống không vượt quá 6 ký tự! ");
                txtMaLoai.grabFocus();
            }
            //Ngược lại thêm bẫy lỗi nếu trùng khoá chính.

            if (sb.length() > 0) {    //Nếu if trên đúng thì sẽ thêm vào sb 1 đoạn string
                //Kiểm tra độ dài chuỗi này nếu lớn hơn  0 tức là có thông báo
                JOptionPane.showMessageDialog(this, sb);
                return;
            } else {
                try {
                    LoaiDoUong ldu = new LoaiDoUong();
                    ldu.setMaLoai(txtMaLoai.getText());
                    ldu.setTenLoai(txtTenLoai.getText());

                    LoaiDoUongDAO lduDao = new LoaiDoUongDAO();
                    lduDao.insert(ldu);
                    JOptionPane.showMessageDialog(this, "Thêm loại đồ uống thành công!");
                    lamTrong();
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Mã loại đồ uống bị trùng lặp!");
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnThemLDUActionPerformed

