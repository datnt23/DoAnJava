    public boolean blockButton(boolean x) {
        if (x == true) {
            btnSuaLDU.setEnabled(false);
            btnXoaLDU.setEnabled(false);
            btnThemLDU.setEnabled(true);
            btnThoatLDU.setEnabled(true);
            return true;
        } else {
            btnSuaLDU.setEnabled(true);
            btnXoaLDU.setEnabled(true);
            btnThemLDU.setEnabled(false);
            btnThoatLDU.setEnabled(false);
            return false;
        }
    }

    private void initTable() {
        tblModelLDU = new DefaultTableModel();
        tblModelLDU.setColumnIdentifiers(new String[]{"MaLoai", "TenLoai"});
        tblLoaiDoUong.setModel(tblModelLDU);
    }

    public void loadData() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            String sql = "Select * from LoaiDoUong";
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblModelLDU.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaLoai"), rs.getString("TenLoai")
                };
                tblModelLDU.addRow(row);
            }
            tblModelLDU.fireTableDataChanged();
            rs.close();
            pstmt.close();
            con.close();
            blockButton(true);
            txtMaLoai.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
            ex.printStackTrace();
        }
    }
