    private void btnXoaLDUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLDUActionPerformed
        try {
            //Ngược lại thêm bẫy lỗi nếu trùng khoá chính.
            StringBuffer sb = new StringBuffer();
            if (sb.length() > 0) {
                JOptionPane.showMessageDialog(this, sb);
            } else {
                int selected = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá chức vụ này không!", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (selected == JOptionPane.YES_OPTION) {
                    LoaiDoUongDAO lduDao = new LoaiDoUongDAO();
                    lduDao.delete(txtMaLoai.getText());
                    JOptionPane.showMessageDialog(this, "Đã xoá loại đồ uống thành công!");
                    lamTrong();
                    loadData();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaLDUActionPerformed

