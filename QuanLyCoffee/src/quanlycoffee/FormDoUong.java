    private void tblDoUongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDoUongMouseClicked
        int row = tblDoUong.getSelectedRow();
        txtMaMon.setEnabled(false);
        blockButton(false);
        if (row >= 0) {
            cbxMaLoai.setSelectedItem(tblDoUong.getModel().getValueAt(row, 0) + "");
            txtMaMon.setText(tblDoUong.getValueAt(row, 1) + "");
            txtTenMon.setText(tblDoUong.getValueAt(row, 2) + "");
            txtGiaMon.setText(tblDoUong.getValueAt(row, 3) + "");
        }
    }//GEN-LAST:event_tblDoUongMouseClicked

    private void btnThemDUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDUActionPerformed
        StringBuffer sb = new StringBuffer();
        if (txtMaMon.getText().equals("") || txtTenMon.getText().equals("") || txtGiaMon.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            //Bẫy lỗi nhập thiếu dữ liệu.
        } else {
            if (txtMaMon.getText().trim().length() > 6) {
                JOptionPane.showMessageDialog(this, "Mã món không vượt quá 6 ký tự! ");
                txtMaMon.grabFocus();
            }
            if (sb.length() > 0) {    //Nếu if trên đúng thì sẽ thêm vào sb 1 đoạn string
                //Kiểm tra độ dài chuỗi này nếu lớn hơn  0 tức là có thông báo
                JOptionPane.showMessageDialog(this, sb);
                return;
            } else {
                try {
                    DoUong du = new DoUong();
                    du.setMaLoai(cbxMaLoai.getSelectedItem().toString());
                    du.setMaMon(txtMaMon.getText());
                    du.setTenMon(txtTenMon.getText());
                    du.setGia(txtGiaMon.getText());

                    DoUongDAO duDao = new DoUongDAO();
                    duDao.insert(du);
                    JOptionPane.showMessageDialog(this, "Thêm món thành công!");
                    lamTrong();
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Mã món trùng lặp!");
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnThemDUActionPerformed

