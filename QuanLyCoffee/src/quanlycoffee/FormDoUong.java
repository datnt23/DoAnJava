    private void btnXoaDUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDUActionPerformed
        try {
            //Ngược lại thêm bẫy lỗi nếu trùng khoá chính.
            StringBuffer sb = new StringBuffer();
            if (sb.length() > 0) {
                JOptionPane.showMessageDialog(this, sb);
            } else {
                int selected = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá món này không!", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (selected == JOptionPane.YES_OPTION) {
                    DoUongDAO duDao = new DoUongDAO();
                    duDao.delete(txtMaMon.getText());
                    JOptionPane.showMessageDialog(this, "Đã xoá món thành công!");
                    lamTrong();
                    loadData();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaDUActionPerformed

