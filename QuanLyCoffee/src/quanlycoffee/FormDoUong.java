    public boolean blockButton(boolean x) {
        if (x == true) {
            btnSuaDU.setEnabled(false);
            btnXoaDU.setEnabled(false);
            btnThemDU.setEnabled(true);
            btnThoatDU.setEnabled(true);
            return true;
        } else {
            btnSuaDU.setEnabled(true);
            btnXoaDU.setEnabled(true);
            btnThemDU.setEnabled(false);
            btnThoatDU.setEnabled(false);
            return false;
        }
    }

    public void loadCombobox() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            PreparedStatement psMaLoai = con.prepareStatement("Select MaLoai from LoaiDoUong group by MaLoai");
            ResultSet rsMaLoai = psMaLoai.executeQuery();
            while (rsMaLoai.next()) {
                cbxMaLoai.addItem(rsMaLoai.getString("MaLoai"));
            }
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }

    private void initTable() {
        tblModelDU = new DefaultTableModel();
        tblModelDU.setColumnIdentifiers(new String[]{"MaLoai", "MaMon", "TenMon", "Gia"});
        tblDoUong.setModel(tblModelDU);
    }

    public void loadData() {
        try {
            DBConnect a = new DBConnect();
            Connection con = a.getConnection();
            String sql = "Select * from DoUong ";
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblModelDU.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaLoai"), rs.getString("MaMon"), rs.getString("TenMon"), rs.getString("Gia")
                };
                tblModelDU.addRow(row);
            }
            tblModelDU.fireTableDataChanged();
            rs.close();
            pstmt.close();
            con.close();
            blockButton(true);
            txtMaMon.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
            ex.printStackTrace();
        }
    }
